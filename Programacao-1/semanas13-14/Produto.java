public class Produto {
    private double preco;

    // Construtores
    public Produto(double preco){
        this.preco = preco;
    }

    // Setters
    public void setPreco(double preco){
        this.preco = preco;
    }

    // Getters
    public double getPreco(){
        return preco;
    }
}
